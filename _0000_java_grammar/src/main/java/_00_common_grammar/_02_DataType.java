package _00_common_grammar;

public class _02_DataType {
    public static void main(String[] args) {
        /**
         * 注意：Java是一种强类型的语言。
         * 1. 基本数据类型
         *    整数型(byte short int long)和浮点型(float double)以及字符型(char)和布尔型(boolean)
         * 2. 引用数据类型：数组(Array)、类(Class)和接口(Interface)
         */
        byte b = 127;
        short s = 1111;
        int i = 11111111;
        long l = 13412343214123L;

        float f = 3.1415926F;
        double d = 3.14159267090;

        char a = 'A';
        boolean bool = true;

    }
}
